function isMoreThanSeven() {
    alert("Составить алгоритм: если введенное число больше 7, то вывести “Привет”")

    inputNumber = prompt("Введите число больше 7")

    if(isNaN(inputNumber)) {
        alert("Введите число")
    }

    if(inputNumber > 7) {
        alert("Привет")
    }

    else {
        alert("Число не больше 7")
    }
}

