function dividedByThree() {
    alert("Составить алгоритм: на входе есть числовой массив, необходимо вывести элементы массива кратные 3. Введите 5 чисел.")

    let arr = [];

    for(let i = 1; i <= 5; i++) {
        arr.push(prompt("Введите " + i + "-e число"));
    }
    
    for (i = 0; i < arr.length; i++){
        if (arr[i] % 3 == 0) {
            alert ((arr[i]) + " кратно трём")
        }
    }
}

